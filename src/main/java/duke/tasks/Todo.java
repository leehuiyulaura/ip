package duke.tasks;

/**
 * To do class to create a To Do Task in the Duke bot.
 * @author lauralee
 */
public class Todo extends Task {

    /**
     * Constructor for the To Do class.
     * @param name
     */
    public Todo(String name) {
        super(name, "T");
    }

    @Override
    public String output() {
        return super.output();
    }
}
